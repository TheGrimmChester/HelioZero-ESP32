#!/usr/bin/env node
/**
 * Render PWA PNG icons from assets/brand/helio-zero-favicon.svg into
 * web/src/brand/pwaIcons.ts (base64 for apple-touch-icon) and
 * web/public/pwa/*.png + manifest.webmanifest + sw.js for firmware routes.
 */
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import sharp from "sharp";

const root = join(dirname(fileURLToPath(import.meta.url)), "../..");
const svgPath = join(root, "assets", "brand", "helio-zero-favicon.svg");
const outTsPath = join(root, "web", "src", "brand", "pwaIcons.ts");
const publicDir = join(root, "web", "public");
const pwaDir = join(publicDir, "pwa");

const SIZES = [
  { name: "PWA_ICON_180", file: null, size: 180 },
  { name: "PWA_ICON_192", file: "icon-192.png", size: 192 },
  { name: "PWA_ICON_512", file: "icon-512.png", size: 512 },
];

const svg = readFileSync(svgPath);
mkdirSync(pwaDir, { recursive: true });

const lines = [
  "// AUTO-GENERATED by web/scripts/generate-pwa-icons.mjs — do not edit.",
  "",
];

for (const { name, file, size } of SIZES) {
  const png = await sharp(svg).resize(size, size).png().toBuffer();
  const b64 = png.toString("base64");
  lines.push(`export const ${name} = "data:image/png;base64,${b64}";`);
  lines.push("");
  if (file) {
    writeFileSync(join(pwaDir, file), png);
  }
}

writeFileSync(outTsPath, lines.join("\n"), "utf8");

const manifest = {
  name: "HelioZero",
  short_name: "HelioZero",
  description: "HelioZero — embedded web interface",
  start_url: "/",
  scope: "/",
  id: "/",
  display: "fullscreen",
  display_override: ["fullscreen", "standalone"],
  background_color: "#0b1220",
  theme_color: "#0b1220",
  icons: [
    { src: "/pwa/icon-192.png", sizes: "192x192", type: "image/png", purpose: "any" },
    { src: "/pwa/icon-512.png", sizes: "512x512", type: "image/png", purpose: "maskable" },
  ],
};

writeFileSync(
  join(publicDir, "manifest.webmanifest"),
  `${JSON.stringify(manifest, null, 2)}\n`,
  "utf8",
);

const swJs = `/* AUTO-GENERATED by web/scripts/generate-pwa-icons.mjs — do not edit. */
const CACHE_VERSION = "__HELIO_UI_VERSION__";
const CACHE_NAME = "helio-ui-" + CACHE_VERSION;
const SHELL_URLS = ["/", "/manifest.webmanifest", "/pwa/icon-192.png", "/pwa/icon-512.png"];

self.addEventListener("install", (event) => {
  event.waitUntil(
    caches.open(CACHE_NAME).then((cache) => cache.addAll(SHELL_URLS)).then(() => self.skipWaiting()),
  );
});

self.addEventListener("activate", (event) => {
  event.waitUntil(
    caches
      .keys()
      .then((keys) =>
        Promise.all(keys.filter((k) => k.startsWith("helio-ui-") && k !== CACHE_NAME).map((k) => caches.delete(k))),
      )
      .then(() => self.clients.claim()),
  );
});

self.addEventListener("fetch", (event) => {
  const url = new URL(event.request.url);
  if (url.pathname.startsWith("/api/")) {
    event.respondWith(fetch(event.request));
    return;
  }
  if (event.request.method !== "GET") {
    return;
  }
  event.respondWith(
    caches.match(event.request).then((cached) => {
      if (cached) return cached;
      return fetch(event.request).then((response) => {
        if (!response || response.status !== 200 || response.type === "opaque") {
          return response;
        }
        const copy = response.clone();
        caches.open(CACHE_NAME).then((cache) => cache.put(event.request, copy));
        return response;
      });
    }),
  );
});
`;

writeFileSync(join(publicDir, "sw.js"), swJs, "utf8");

console.log(`generate-pwa-icons: wrote ${outTsPath}, public/pwa, manifest.webmanifest, sw.js`);
